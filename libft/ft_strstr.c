char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = -1;
	j = 0;
	while (to_find[j] != '\0')
		j++;
	if (j == 0)
		return (str);
	while (str[++i] != '\0')
	{
		j = 0;
		while (to_find[j] == str[i + j])
		{
			if (to_find[j + 1] == '\0')
				return (str + i);
			j++;
		}
	}
	return (0);
}
