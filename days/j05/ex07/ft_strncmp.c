int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = -1;
	while (++i < (int)n && *s1++ == *s2++)
		if (*s1 == '\0' && *s2 == '\0')
			return (0);
	return (*--s1 - *--s2);
}
