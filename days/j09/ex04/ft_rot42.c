#include <stdio.h>

char	rot42char(char c)
{
	if (c >= 65 && c <= 90)
	{
		c = c - 65;
		c = ((c + 42) % 26);
		c = c + 65;
		return (c);
	}
	else if (c >= 97 && c <= 122)
	{
		c = c - 97;
		c = ((c + 42) % 26);
		c = c + 97;
		return (c);
	}
	return (c);
}

char	*ft_rot42(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		str[i] = rot42char(str[i]);
		i++;
	}
	return (str);
}
